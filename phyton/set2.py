# 1. difference():

set1 = {1, 2, 3, 4}
set2 = {3, 4, 5}
result = set1.difference(set2)  # result is {1, 2}

# 2. difference_update():
set1 = {1, 2, 3, 4}
set2 = {3, 4, 5}
set1.difference_update(set2)  # set1 is now {1, 2}

# 3. discard():
my_set = {1, 2, 3}
my_set.discard(2)  # my_set is now {1, 3}

     


     

     

     
