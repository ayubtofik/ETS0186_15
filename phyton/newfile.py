# Example List
my_list = [1, 2, 3, 2, 4, 2, 5]

# 1. Using count()
count_of_twos = my_list.count(2)
print( count_of_twos ) #trying to see how many times does the number two appears in the list  # Output:  3 

#2. Using index()
index_of_first_two = my_list.index(2)
print(f"The first occurrence of 2 is at index {index_of_first_two}.")  # Output: The first occurrence of 2 is at index 1.
    
   
