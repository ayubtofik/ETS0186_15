#  1. reverse()

my_list = [1, 2, 3, 4, 5]

# Reverse the list
my_list.reverse()

# Print reversed list
print("Reversed list:", my_list)

# output: Reversed list: [5, 4, 3, 2, 1]

# 2. sort()

number_list = [5, 2, 9, 1, 5, 6]

number_list.sort()

print("Sorted list:", number_list)

# output: Sorted list: [1, 2, 5, 5, 6, 9]


