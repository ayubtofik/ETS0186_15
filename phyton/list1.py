# 1. append()

fruits = ['apple', 'banana', 'cherry']

fruits.append('orange')

print(fruits)  # Output: ['apple', 'banana', 'cherry', 'orange']

# 2. clear()

# Create a list
numbers = [1, 2, 3, 4, 5]

numbers.clear()

print(numbers)  # Output: []

# 3. copy()

# Create a list
original_list = [1, 2, 3]

# Copy the list
copied_list = original_list.copy()

print(copied_list)  # Output: [1, 2, 3]



